//Crear Clase Persona

class Persona{

//Crear Atributos de la Clase

	private String rut;
	private String nombre;

//Crear Metodos

	//Metodo setRut

	public void setRut(String rut){
		this.rut = rut;
	}

	//Metodo getRut

	public String getRut(){
		return rut;
	}

	//Metodo setNombre

	public void setNombre(String nombre){
		this.nombre = nombre;
	}

	//Metodo getNombre

	public String getNombre(){
		return nombre;
	}

}